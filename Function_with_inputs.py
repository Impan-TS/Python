# Function with String input
def greet(name):
    print("Hi "+name)
    print("Welcome " + name)
greet("Ravi")


# Function with Integer input
def greeting(num):
    print(f"Hi {num}")
    print(f"Welcome {num}")
greeting(123)


# Function with Multiple input,
def greeting(name,loc):
    print(f"Hi {name}")
    print(f"Welcome {loc}")
greeting("Ram","Mysore")